#include "so_long.h"

int ft_count_coin(char **map)
{
    int i;
    int j;
    int k;

    j = 0;
    k = 0;
    while (map[j])
    {
        i = 0;
        while(map[j][i])
        {
            if(map[j][i] == 'C')
                k++;
            i++;
        }
        j++;
    }
    return (k);
}
